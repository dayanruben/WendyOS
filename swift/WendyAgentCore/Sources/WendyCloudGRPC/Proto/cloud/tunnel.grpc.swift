// TunnelBrokerService brokers mTLS-authenticated TCP tunnels between wendy-agent
// (device) and wendy CLI through the cloud relay.
//
// Authentication: all three RPCs require a valid mTLS client certificate.
//   - Agent RPCs (RegisterPresence, AgentTunnel): asset certificate (org_id + asset_id in SAN)
//   - CLI RPC (ClientTunnel): user certificate (org_id + user_id in SAN)
//
// Session lifecycle:
//   1. CLI sends ClientTunnelOpen (asset_id, host, port) as the first message on a
//      ClientTunnel stream. No session_id exists at this point.
//   2. Broker generates a session_id, parks the ClientTunnel stream, and pushes a
//      DialRequest (session_id, host, port) to the target agent via RegisterPresence.
//   3. Agent dials the local service, opens an AgentTunnel stream, and sends
//      TunnelData{session_id} (empty payload) as its first message to claim the session.
//   4. Broker pairs the two streams by session_id and begins relaying payload data
//      transparently in both directions. session_id is ignored in all subsequent messages.

